import type { Player, RugbyPositionId, Squad } from '../types/rugby'

type PlayerSeed = [string, RugbyPositionId, number, number, number, number, number]

const shirtNumbers: Record<RugbyPositionId, number> = {
  LH: 1,
  HK: 2,
  TH: 3,
  LO4: 4,
  LO5: 5,
  BF: 6,
  OF: 7,
  N8: 8,
  SH: 9,
  FH: 10,
  LW: 11,
  IC: 12,
  OC: 13,
  RW: 14,
  FB: 15,
}

const slug = (value: string) =>
  value
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/(^-|-$)/g, '')

const makeSquad = (
  id: string,
  country: string,
  countryCode: string,
  flag: string,
  year: number,
  overall: number,
  seeds: PlayerSeed[],
): Squad => ({
  id,
  country,
  countryCode,
  flag,
  year,
  tournament: 'World Cup',
  overall,
  players: seeds.map(([name, position, playerOverall, attack, defense, kicking, discipline]): Player => ({
    id: `${slug(name)}-${year}`,
    name,
    country,
    countryCode,
    flag,
    year,
    tournament: 'World Cup',
    shirtNumber: shirtNumbers[position],
    position,
    overall: playerOverall,
    attack,
    defense,
    kicking,
    discipline,
    speed: Math.min(99, attack + (['LW', 'RW', 'FB'].includes(position) ? 6 : 0)),
    power: Math.min(99, defense + (['LH', 'HK', 'TH', 'LO4', 'LO5', 'N8'].includes(position) ? 5 : 0)),
    leadership: Math.round((playerOverall + discipline) / 2),
  })),
})

